package vo;

public class TalentOptionDataType {
	int product_num, op_num, op_date, op_price;
	String op_title, op_descript;
	
	public int getProduct_num() {
		return product_num;
	}
	public void setProduct_num(int product_num) {
		this.product_num = product_num;
	}
	public int getOp_num() {
		return op_num;
	}
	public void setOp_num(int op_num) {
		this.op_num = op_num;
	}
	public int getOp_date() {
		return op_date;
	}
	public void setOp_date(int op_date) {
		this.op_date = op_date;
	}
	public int getOp_price() {
		return op_price;
	}
	public void setOp_price(int op_price) {
		this.op_price = op_price;
	}
	public String getOp_title() {
		return op_title;
	}
	public void setOp_title(String op_title) {
		this.op_title = op_title;
	}
	public String getOp_descript() {
		return op_descript;
	}
	public void setOp_descript(String op_descript) {
		this.op_descript = op_descript;
	}
	
}
