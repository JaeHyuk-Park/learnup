package vo;

public class User
{
	private String name;
	private String id;
	private int num;

	public String getName(){
		return name;
	}
	public void setName(String name){
		this.name = name;
	}
	public String getId(){
		return id;
	}
	public void setId(String id){
		this.id = id;
	}
	public int getNum(){
		return num;
	}
	public void setNum(int num){
		this.num = num;
	}
}